async function createTempRoot(): Promise<string> {
  return mkdtemp(path.join(tmpdir(), "ap-migration-plan-"));
}

// The Phase 16 plan endpoint is now the same write-plan logic the CLI uses.
// Most regions/adoption tests therefore need a profile on disk so compile
// can produce generated bytes; without one, the plan refuses with
// requires-profile. This fixture writes a minimal valid profile that the
// compiler accepts.
const FIXTURE_PROFILE = `version: 1
profile:
  name: migration-plan-test
  description: Migration plan fixture.
stack:
  languages: [typescript]
  frameworks: [sveltekit]
  packageManagers: [npm]
  testing: []
clients:
  tabnine: { enabled: false }
  codex: { enabled: true }
  claude: { enabled: true }
workflow:
  sdd: true
  tdd: true
  finalReview: false
`;

async function writeFixtureProfile(root: string): Promise<void> {
  await writeFile(path.join(root, "ai-profile.yaml"), FIXTURE_PROFILE, "utf8");
}

test("preserve plan snapshot: zero writes, all rows resolved as preserve", async () => {
  const root = await createTempRoot();
  await writeFile(path.join(root, "AGENTS.md"), "manual body\n", "utf8");

  const plan = await buildMigrationPlan(root, [
    { path: "AGENTS.md", action: "preserve" },
    { path: "CLAUDE.md", action: "preserve" },
  ]);

  assert.equal(plan.writes.length, 0);
  assert.equal(plan.refusals.length, 0);
  assert.deepEqual(
    plan.resolved.map((r) => ({ path: r.path, action: r.action })),
    [
      { path: "AGENTS.md", action: "preserve" },
      { path: "CLAUDE.md", action: "preserve" },
    ],
  );
});

test("regions plan snapshot: existing AGENTS.md wrapped with compiled generated bytes", async () => {
  const root = await createTempRoot();
  await writeFixtureProfile(root);
  await writeFile(path.join(root, "AGENTS.md"), "manual body\n", "utf8");

  const plan = await buildMigrationPlan(root, [
    { path: "AGENTS.md", action: "add-regions" },
  ]);

  assert.equal(plan.refusals.length, 0);
  assert.equal(plan.writes.length, 1);
  const write = plan.writes[0];
  assert.equal(write.path, "AGENTS.md");
  const buffer = Buffer.from(write.bytes);
  assert.ok(hasAllRegionMarkers(buffer), "all region markers present");
  const text = buffer.toString("utf8");
  assert.ok(text.includes(GENERATED_START_MARKER));
  assert.ok(text.includes(GENERATED_END_MARKER));
  assert.ok(text.includes(MANUAL_START_MARKER));
  assert.ok(text.includes(MANUAL_END_MARKER));
  const parsed = parseMixedFile(buffer);
  assert.equal(parsed.ok, true);
  if (parsed.ok) {
    // Manual region preserves the original bytes verbatim.
    assert.equal(parsed.manualInner.toString("utf8"), "manual body\n");
    // Generated region is non-empty now that we run real compile.
    assert.ok(parsed.generatedInner.length > 0);
  }
});

test("regions plan does not emit a write for a missing root file", async () => {
  // CLI parity: planRegionAdoptions skips when the on-disk file is absent.
  // The web plan must do the same instead of inventing an empty-region file.
  const root = await createTempRoot();
  await writeFixtureProfile(root);

  const plan = await buildMigrationPlan(root, [
    { path: "AGENTS.md", action: "add-regions" },
  ]);

  assert.equal(
    plan.writes.length,
    0,
    "missing file must not produce a synthesized write",
  );
  const refusal = plan.refusals.find((r) => r.path === "AGENTS.md");
  assert.equal(refusal?.reason, "missing-file");
});

